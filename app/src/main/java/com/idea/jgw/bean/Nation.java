public class Nation {
    /**
     * en : Yugoslavia
     * zh : 南斯拉夫
     * locale : YU
     * code : 381
     */

    private String en;
    private String zh;
    private String locale;
    private int code;

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getZh() {
        return zh;
    }

    public void setZh(String zh) {
        this.zh = zh;
    }

    public String getLocale() {
        return locale;
    }

    public void setLocale(String locale) {
        this.locale = locale;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
